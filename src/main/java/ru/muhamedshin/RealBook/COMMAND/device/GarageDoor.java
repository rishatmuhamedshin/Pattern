package ru.muhamedshin.RealBook.COMMAND.device;

public class GarageDoor {
    public void open() {
        System.out.println("Открыть двери");
    }

    public void closed() {
        System.out.println("Закрыть двери");
    }

    public void stop() {
        System.out.println("Двери остановлены");
    }

    public void lightOn() {
        System.out.println("Свет включен");
    }

    public void lightOff() {
        System.out.println("Свет выключен");
    }
}
